#include <stdio.h>

// Hindi function to add two numbers
int जोड़(int a, int b) {
    return a + b;
}

int main() {
    // Hindi greeting
    printf("नमस्ते दुनिया!\n");
    
    // Hindi variables
    int संख्या1 = 10;
    int संख्या2 = 5;
    
    // Using Hindi function
    int परिणाम = जोड़(संख्या1, संख्या2);
    
    // Display result with Hindi text
    printf("जोड़: %d + %d = %d\n", संख्या1, संख्या2, परिणाम);
    
    return 0;
}
